/**
 * Canonical internal architecture graph for Assistant-MK1.
 *
 * This is the source of truth for generated architecture views. Keep graph
 * schema shape and graph content together here while the renderer handles only
 * validation and output. Maintainers should not need to edit another graph
 * source file when adding nodes, edges, kinds, tags, or views.
 *
 * Update this file when a change affects a tracked file/doc responsibility,
 * runtime boundary, durable contract, workflow, policy, storage ownership, or
 * current/target status. Then run `pnpm graph:render` and `pnpm graph:check`.
 */

