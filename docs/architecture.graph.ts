export type ArchitectureNodeKind = (typeof architectureNodeKinds)[number];

export type ArchitectureEdgeKind = (typeof architectureEdgeKinds)[number];

export type ArchitectureNode = {
  id: string;
  label: string;
  summary: string;
  kind: ArchitectureNodeKind;
  tags: string[];
  fileRefs?: string[];
  notes?: string;
};

export type ArchitectureEdge = {
  id: string;
  from: string;
  to: string;
  kind: ArchitectureEdgeKind;
  label: string;
  tags?: string[];
  notes?: string;
};

export type ArchitectureView = {
  id: string;
  title: string;
  summary: string;
  nodeIds: string[];
  edgeIds: string[];
  direction?: "LR" | "TD";
};

export type ArchitectureGraph = {
  id: string;
  title: string;
  summary: string;
  nodes: ArchitectureNode[];
  edges: ArchitectureEdge[];
  views: ArchitectureView[];
};

function defineArchitectureGraph(graph: ArchitectureGraph): ArchitectureGraph {
  return graph;
}

